function SVG(id, name) {
    const i = document.getElementById(id);
    if (i === null || i === undefined)
        return;

    let svg = null;
    switch (name) {
        case "github":
            svg = github();
        break;
        case "academic_cap":
            svg = academic_cap();
        break;
        case "wrench_screwdriver":
            svg = wrench_screwdriver();
        break;
        case "question_mark":
            svg = question_mark();
        break;
        case "arrow_left_circle":
            svg = arrow_left_circle();
        break;
        case "arrow_right_circle":
            svg = arrow_right_circle();
        break;
        case "arrow_top_right_on_square":
            svg = arrow_top_right_on_square();
        break;
        case "table_cells":
            svg = table_cells();
        break;
        case "arrow_down_on_square_stack":
            svg = arrow_down_on_square_stack();
        break;
        case "pencil":
            svg = pencil();
        break;
        case "trash":
            svg = trash();
        break;
        case "play":
            svg = play();
        break;
        case "stop":
            svg = stop();
        break;
        case "arrow_path":
            svg = arrow_path();
        break;
        default:
            svg = x_mark();
        break;
    }

    svg.setAttribute("class", i.className);
    i.replaceWith(svg);
}

